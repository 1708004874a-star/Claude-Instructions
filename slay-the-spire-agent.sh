#!/bin/bash
# Slay the Spire 自动化脚本 - 使用 Mano-P 云模式持续游戏
# 用法: ./slay-the-spire-agent.sh [回合数默认50]

MAX_TURNS=${1:-50}
GAME_WINDOW="Slay the Spire"

echo "=== 杀戮尖塔 AI Agent ==="
echo "总回合数: $MAX_TURNS"
echo "按 Ctrl+C 停止"
echo ""

for i in $(seq 1 $MAX_TURNS); do
    echo "=== 第 $i 步 ==="

    mano-cua run "你在玩杀戮尖塔，使用铁甲战士（Ironclad）。看屏幕判断当前游戏阶段：

如果是战斗阶段：
- 观察手牌（每张牌的费用和效果）、当前能量、自己HP和格挡、敌人HP和意图
- 策略：如果敌人这回合要攻击且伤害高（>8），优先出防御牌格挡。否则出攻击牌。
- 出牌方式：左键点击手牌选中，再左键点击敌人打出。不要拖拽。不要按Escape。不要右键。
- 优先击杀血量最低的敌人（减少敌方数量优于贪伤害）
- 出完所有能出的牌后，点击"结束回合"

如果是地图阶段：
- 查看可选路径，选择奖励多、避开精英的路线
- 优先：休息（血少时）> 战斗（普通敌人）> 商店 > 未知
- 点击目标节点

如果是选牌奖励阶段：
- 评估三张牌，选择对战士力量流有帮助的牌
- 都不好就点"跳过"

如果是商店：
- 评估遗物和卡牌，有用的就买
- 然后点"离开"

如果是事件：
- 阅读文字，选择风险最低、收益最高的选项
- 然后点"离开"

如果是主菜单或游戏结束画面：
- 点击"继续"或"主菜单"回到正常流程

保持简洁决策，快速行动。不要反复分析，每个回合只做核心操作。" \
    --minimize --max-steps 8

    echo "---"
    sleep 3  # 等待游戏动画
done

echo "=== 自动化完成 ==="
