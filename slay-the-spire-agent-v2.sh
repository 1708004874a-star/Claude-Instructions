#!/bin/bash
# Slay the Spire 自动化脚本 v2 - 嵌入游戏知识库，单次调用优化
# 用法: ./slay-the-spire-agent-v2.sh [回合数默认50]

MAX_TURNS=${1:-50}

echo "=== 杀戮尖塔 AI Agent v2 ==="
echo "总回合数: $MAX_TURNS"
echo "按 Ctrl+C 停止"
echo ""

for i in $(seq 1 $MAX_TURNS); do
    echo "=== 第 $i 步 ==="

    mano-cua run "你是杀戮尖塔专家AI，控制铁甲战士。每个回合：先仔细观察屏幕，在心里分析状态，然后做出最优操作。

## 游戏知识

铁甲战士基础牌：打击(1费,6伤害)、防御(1费,5格挡)、痛击(2费,8伤害+1层易伤)
常见铁甲战士卡牌：双重打击(1费,5x2)、愤怒(0费,6伤害)、剑柄打击(1费,10伤害)、耸肩无视(1费,8格挡+抽1)、燃烧(2费,力量+2)、观察弱点(1费,若敌意图攻击则力量+3)、威吓(0费,全体1层易伤)、完美打击(1费,卡组每张打击+2)、全身撞击(1费,造成等于格挡值的伤害)

敌人意图图标含义：红色剑+数字=攻击该伤害值；绿色盾=防御；蓝色箭头=强化；紫色螺旋=debuff

## 战斗策略

优先级：
1. 如果快死了（HP<25%），优先出防御牌保命
2. 优先杀掉血量最低的敌人（减少数量 > 贪伤害）
3. 如果有痛击：最先出它，让后续攻击享受150%伤害
4. 如果有0费牌（愤怒、威吓）：无成本使用，可以先用
5. 计算伤害：确保攻击伤害加起来 ≥ 敌人HP才能打死
6. 如果敌人这回合攻击伤害高（>10），用防御牌
7. 出完能出的牌后，点击结束回合

## 地图策略
- 低血量(<50%HP)：优先休息 > 普通战斗 > 商店 > 未知
- 高血量：精英(好遗物) > 战斗 > 商店 > 休息
- 商店前尽量有150+金币
- 尽量走能到达最多节点的路线

## 选牌策略（力量流战士）
好牌优先拿：燃烧、观察弱点、剑柄打击、耸肩无视
可以拿：双重打击、威吓、武装、愤怒
不推荐：太多纯防御牌

## 操作规则
- 只左键操作：点牌选中 → 点目标打出
- 禁止拖拽、禁止按Escape、禁止右键
- 战斗出完牌后必须点结束回合
- 地图上点节点前进
- 选牌时评估后选择或跳过
- 事件阅读后选最优选项

保持高效。每步最多分析10秒，然后行动。" \
    --minimize --max-steps 10

    echo "---"
    sleep 2
done

echo "=== 自动化完成 ==="
